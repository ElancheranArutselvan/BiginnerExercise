public class Forloop {
    public static void main(String[] args) {
        for(int a = 0; a<=100; a += 10){
            System.out.println(a);
        }
        String[] sports = {"Cricket", "Kabadi", "Football", "Vollyball"};
        for(String i : sports){
            System.out.println(i);
        }
    }
}